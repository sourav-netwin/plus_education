								<u>Level 1- Elementary:</u><br>
								The course covers basic vocabulary such as places, families, jobs, times, interests and hobbies, food and drink. It introduces grammar for initial communication. Students also learn to introduce themselves, talk about jobs, ask questions, give a description, plan a trip, and talk about their likes and dislikes.<br>
								<br><u>Level 2- Pre- intermediate:</u><br>
								This level builds on students’ vocabulary, revising and developing lexis for more sophisticated interaction. Students learn to communicate on a variety of topics: talking about daily lives and routines, making comparisons, talking about free-time activities and life experiences, describing activities, people and feelings, talking about past events.<br>
								<br><u>Level 3- Intermediate:</u><br>
								At this level students are taught a range of techniques to increase their vocabulary while grammatical concepts are revised and reinforced. Skills and functions include: introducing themselves and each other, discussing differences, planning a trip, completing and collating surveys, making complaints, giving advice and creating an advertisement.<br>
								<br><u>Level 4- Upper-Intermediate:</u><br>
								Students are expected to refine and develop vocabulary topics and areas of grammatical competency. Functions include: asking for and giving personal information, making future predictions, participating in an interview, talking about character and emotions, discussing hypothetical situations, writing a magazine article.<br>
								<br><u>Level 5- Advanced:</u><br>
								At this level students take an active role in discovering which areas of language they need to work on and improve, and to learn ways of doing this effectively. Students work with authentic texts and begin to identify aspects of phonology such as word stress and intonation. Functions covered include: debating, hypothesising, evaluating, identifying and participating in debates on contemporary topics.<br>
